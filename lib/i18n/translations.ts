export function getTranslations(locale: Locale) {
  return translations[locale] || translations.tr;
}

export function getLocaleFromPath(pathname: string): Locale {
  if (pathname.startsWith('/en')) return 'en';
  if (pathname.startsWith('/ar')) return 'ar';
  if (pathname.startsWith('/ru')) return 'ru';
  if (pathname.startsWith('/fr')) return 'fr';
  return 'tr';
}

export function getPathWithLocale(path: string, locale: Locale): string {
  // Remove existing locale prefix if any
  let pathWithoutLocale = path.replace(/^\/(en|ar|ru|fr)/, '');
  // If path is empty after removing locale, set to root
  if (!pathWithoutLocale || pathWithoutLocale === '') {
    pathWithoutLocale = '/';
  }
  // Ensure path starts with /
  const cleanPath = pathWithoutLocale.startsWith('/') ? pathWithoutLocale : `/${pathWithoutLocale}`;
  
  if (locale === 'tr') return cleanPath;
  return `/${locale}${cleanPath}`;
}

